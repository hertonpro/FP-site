                <h3 id="org">Chap1. ORGANISATION DE LA MAISON DORCAS</h3>
                Sous la supervision du conseil d’exécution de la Fondation Panzi, la coordination de la Maison DORCAS
                assure sa gestion globale et quotidienne à travers cinq départements gérés chacun par un superviseur
                <ol>
                    <strong>
                        <li>Département de transit</li>
                    </strong>
                </ol>
                Département mère de la Maison DORCAS, qui accueille et prépare la réinstallation des bénéficiaires dans
                leurs communautés respectives. Lieu de regroupement pour réduire les tendances qui les opposent et le
                stigma dont souffre la plupart des bénéficiaires à l’entrée. Le transit n’a pas de discrimination ayant
                trait à l’origine ou à la tribu.
                3 services sont organisés : <br><br>
                <strong>a) Service d’hébergement :</strong> <br><br>
                Le service d’hébergement est celui qui accueille les bénéficiaires, les encadrent durant tout leur séjour,
                se charge de leurs problèmes etc. Maison Dorcas offre d’abord un hébergement/logement à court-terme aux
                femmes qui ne peuvent pas retourner chez elles immédiatement après leur traitement à l’hôpital de Panzi.
                Pour que les femmes et les filles soient capables de réintégrer leurs communautés, il n’est pas souhaitable
                que leur séjour soit long à la Maison Dorcas, mais il s’observe des exceptions au cas-par-cas. Certaines
                d’entre elles y restent pendant leur reconstitution physique après ou entre les opérations, les autres y
                sont pour raison de sécurité/protection, d’autres y restent parce que le processus de médiation familiale et
                communautaire n’est pas encore à sa fin. Les femmes vivant loin de Panzi peuvent séjourner à la Maison
                Dorcas pendant quelques mois pour être à mesure de répondre au rendez-vous de suivi à l’hôpital. Les
                activités du département : - logement : un logement adéquat est offert aux ayants droits (femmes et filles)
                internes avec une capacité de 100 lits. - Alimentation : les ayants droits sont nourris (trois repas par
                jour) dans un environnement protégé pour qu’elles profitent au mieux des services leurs pourvus.
                -Socialisation : A l’accueil au centre, l’assistante social se charge d’expliquer aux ayants droit la Maison
                DORCAS, son fonctionnement, ses normes. Initiés dans des découvertes du monde à travers des projections, des
                émissions au télévisions, des CD éducateurs, et des sorties de découvertes. -Protection : Les ayant droits
                bénéficient des kits d’hygiène, des habits, des souliers, et une vie protégée dans l’enclos du centre. <br>
                <strong>b) Service psychosociale :</strong> <br><br>
                Le séjour d’un malade à l’hôpital étant très réduit, la prise en charge se poursuit dans le pilier
                socio-économique. Quelques approches développées : <br><br>
                <ul>
                    <li><strong>La prise en charge classique</strong> à travers des consultations psychologiques
                        individuelles, diagnostiques et administration des psychothérapies par un psychologue clinicien.
                    </li>
                    <li><strong>Les thérapies de masses tel que :</strong>La Musico thérapie ou l’art de guérir par la
                        Musique, Les sorties récréatives : les sorties récréatives nous permettent de faire voyager la
                        pensée et donne un nouveau souffle, Les activités sportives (Foot Ball, Danse thérapie, Karaté) :
                        permettent une reconstruction physique qui débouche sur une décharge émotionnelle.
                    </li>
                </ul>
                <strong>c) Service Médical :</strong> <br><br>
                Les bénéficiaires des activités de la Maison DORCAS sont suivis médicalement par une unité médicale. Trois
                infirmières sont disponibles et le service fonctionne 24/24h chaque jour mais cette année l’unité a
                fonctionné avec une infirmière accompagnée par une assistante psycho sociale. Les examens de base sont
                faits, l’administration des médicaments, et la référence à l’hôpital général de référence de Panzi pour des
                cas graves.
                <ol start="2">
                    <li>Département D’Apprentissage :</li>
                </ol>
                Le département organise des formations suivantes : La coupe et Couture, La broderie, Saponification,
                Informatique, Pâtisserie, soins de beauté, vannerie, et l’alphabétisation, étant donné que la plupart des
                femmes et filles qui nous arrivent ne savent ni lire et écrire. La durée est fonction de la filière choisie
                (six mois et douze mois). Un brevet de formation et un kit de réinsertion est remis à chaque bénéficiaire
                qui a fini la formation pour commencer l’activité génératrice de revenus dans son milieu d’origine.
                <ol start="3">
                    <li>Département de protection de l’enfance : </li>
                </ol>
                Les ayants droits du programme de la Maison DORCAS sont constitués des femmes et filles dont la plupart sont
                de moins de 18 ans qui ont été abusés. Plus ou moins 75% des internes viennent avec des enfants et 5% des
                cas avec grossesses accouchent au centre. Il s’ensuit des SGBV et vulnérables qui suivent des formations en
                externe qui ont des enfants.

                Puis que nous ne pouvons pas séparer les enfants de leurs mères dans la prise en charge, les enfants restent
                à Maison Dorcas ensemble avec leurs mères si elles ne peuvent pas les laisser sous la responsabilité de
                quelqu’un d’autre. Non seulement cela leur permet de participer et profiter de l’assistance malgré la charge
                des enfants dépendants mais aussi une prise en charge de ceux qui éprouvent un problème psycho social.
                Services organisés : <br>
