                <h3 id="cible">Chap 2. GROUPES CIBLES</h3>
                Les bénéficiaires de la Maison DORCAS sont généralement des femmes et filles survivants des violences
                sexuelles, et celles basées sur le genre ainsi que des femmes vulnérables environnant le centre. Il s’agit
                des femmes et filles enceintes qui attendent l’accouchement, les accouchées qui attendent la reconstruction
                physique et la formation en métiers, celles qui sont accompagnées de leurs enfants en âge scolaire, les
                enfants abandonnés par leurs parents, les femmes et filles porteuses des fistules, les séropositives à cause
                du viol subi etc. La plupart de ces filles ont abandonné leurs études sans le vouloir (à cause du viol) mais
                elles ont des rêves pour l’avenir. Cette année le département d’hébergement a pu accompagner 72 personnes
                dont 14 enfants et 58 femmes et filles. Il s’agit de 17 personnes de plus de 17ans et 55 personnes dont
                l’âge est inférieur ou égal à 17ans. Considérant le département d’apprentissage nos services ont été offerts
                à 125 ayant droits dans le cantre. Il s’agit de 80 personnes en éducation informelle et en métiers
                professionnels ainsi que 45 autres dans l’éducation formelle. Elles ont bénéficié d’autres formations
                supplémentaires sur les activités génératrices de revenus, la gestion des groupes solidaires, le markéting à
                travers le module de compétence d’affaire. Plus ou moins 800 personnes reparties dans 32 mutuelles de
                solidarité et 35 réparties dans 5 micro-centres existants. De manière générale les bénéficiaires directes
                c’est-à-dire ceux qui ont bénéficié des services directement de la Maison DORCAS s’élève à : 988 personnes.
