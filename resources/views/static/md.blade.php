                solidaire appelé communément « Mutuelle de solidarité ». L’activité génératrice de revenue entretenue par
                chacun de membre lui permet d’intervenir efficacement dans le groupe. Se basant sur la théorie de MUSO et
                notre spécificité sur l’approche, les membres travaillent sur leurs questions, à travers leurs cotisations
